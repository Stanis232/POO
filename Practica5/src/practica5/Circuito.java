public class Circuito {
    private double[] recta,curva;

    public Circuito(double[] distanciarecta, double[] distanciacurva) {
        this.recta = new double[recta.length];
        this.curva = new double[curva.length];;
    }

    public double[] getDistanciarecta() {
        return recta;
    }

    public void setDistanciarecta(double[] distanciarecta) {
        this.recta = distanciarecta;
    }

    public double[] getDistanciacurva() {
        return curva;
    }

    public void setDistanciacurva(double[] distanciacurva) {
        this.curva = distanciacurva;
    }
    
}
